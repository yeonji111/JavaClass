package ch01_start; // 파일의 위치

// 실제 파일명과 class 옆에 적혀있는 클래스명이 일치해야함
public class HelloWorld {

    // main 입력 후 엔터 = main 메소드 생성
    public static void main(String[] args) {
        //코드 실행 [쉬프트 + F10] , main 메소드의 중괄호 내부에 작성된 코드가 실행된다.

        //콘솔창에 텍스트 출력


        //so 입력하면 자동완성 메소드가 생김, so 치고 sout라 적힌거 엔터하면 System.out.println 자동완성
        System.out.println("HelloWorld");


        // 유용한 단축키(툴마다 다른 경우가 많다)
        // 코드 라인 복사 [Ctrl + D]
        // 코드 라인 삭제 [Ctrl + Y]
        // 실행 취소 [Ctrl + Z]
        // 실행 취소 되돌리기(Redo) [Ctrl + Shift + Z]
        // 자동완성 [Ctrl + Space]

        System.out.println("박연지");
        System.out.println("박연지");

    }
}
